using namespace std;

int n;
int stone[10000 + 10][2], ans;

int main(){
    cin>>n;
	for (int i = 0,temp; i < n * 2; i++)
    {
        cin>>temp;
	    if (stone[temp][0] == 0)//没有记录过
	        stone[temp][0] = i+1;
	    else stone[temp][1] = i+1;//记录过
	}
	ans = stone[1][0] + stone[1][1] - 2;
	for (int i = 2; i <= n; i++)
	{
		ans += min (abs (stone[i][0] - stone[i - 1][0]) + abs (stone[i][1] - stone[i - 1][1]),
         abs (stone[i][0] - stone[i - 1][1]) + abs (stone[i][1] - stone[i - 1][0]));
	}
	cout<<ans<<endl;
} 
